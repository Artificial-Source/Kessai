/** Get the bundled logo path for a template domain */
export function getTemplateLogo(domain: string | null): string | null {
  if (!domain) return null
  const slug = domain.replace(/[^a-zA-Z0-9]/g, '-')
  return `/logos/templates/${slug}.png`
}

